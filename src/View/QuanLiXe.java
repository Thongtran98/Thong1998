    private void luuVeThangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luuVeThangActionPerformed
        // TODO add your handling code here:
        VeThang vthang  = new VeThang();
        vthang.setHoTen(nhapHoTen.getText());
        vthang.setMaVe(nhapMaVeThang.getText());
        vthang.setMaXe(nhapMaXeThang.getText());
        vthang.setLoaiXe(LoaiXe3.getSelectedItem().toString());
        //kiểm tra ngoại lệ
        if(vthang.getHoTen().length()==0)
        {
            JOptionPane.showMessageDialog(null,"Bạn chưa nhập họ tên");
            
        }
        else if(vthang.getMaVe().length()==0)
        {
            JOptionPane.showMessageDialog(null,"Bạn chưa nhập mã vé");
        }
        else if(vthang.getMaVe().startsWith("VT") == FALSE)
        {
            JOptionPane.showMessageDialog(null,"Mã vé ngày phải bắt đầu bằng VT ");
        }
        else if(vthang.getMaXe().length()==0)
        {
            JOptionPane.showMessageDialog(null,"Bạn chưa nhập mã xe");
        }
        else{
        // ngày làm vé lấy ngày giờ hệ thống
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        
        // tạo 1 đối tượng có định dạng thời gian yyyy-MM-dd
        Date dateVao = new Date(); // lấy thời gian hệ thống
        String stringDate = dateFormat.format(dateVao);
        vthang.setNgayLam(stringDate);
        //ngày hết hạn
        String thoihan = thoiHan.getSelectedItem().toString();
        vthang.tinhThoiHan(vthang, dateVao, thoihan);
        noiVeThang.addVeThang(vthang);    
        }
    }//GEN-LAST:event_luuVeThangActionPerformed

