    private void kiemTraVeRaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kiemTraVeRaActionPerformed
        // TODO add your handling code here:
        String mave = nhapMaVeRa.getText(); // lấy mã vé
        if(mave.length()==0)
        {
            JOptionPane.showMessageDialog(null,"Bạn chưa nhập mã vé ra");
        }
        else if(mave.startsWith("VN") || mave.startsWith("vn")){               
            VeNgay vn = new VeNgay();
            if(noiVeNgay.KiemTraMV( mave))
            {
                KiemTraMaVe ktmv = new KiemTraMaVe();
                ktmv.setVisible(true); 
                noiVeNgay.TimVeNgay(vn, mave);
                ktmv.txtMaVe.setText(vn.getMaVe());
                ktmv.txtMaXe.setText(vn.getMaXe());
                ktmv.txtLoaiXe.setText(vn.getLoaiXe());
                ktmv.txtTGVao.setText(vn.getNgayGui());
                DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                // tạo 1 đối tượng có định dạng thời gian dd-MM-yyyy
                Date dateRa = new Date(); // lấy thời gian hệ thống
                String stringDateRa = dateFormat.format(dateRa);
                ktmv.txtTGRa.setText(stringDateRa);
                //tính số ngày xe gửi
                int sotien;
                sotien = vn.tinhTienVN(vn, vn.getNgayGui(), stringDateRa);
                ktmv.txtSoTien.setText(String.valueOf(sotien));
                ktmv.setVisible(true); 
            }
            else{
            JOptionPane.showMessageDialog(null,"Mã vé không tồn tại");
            }  
        }
        else if(mave.startsWith("VD") || mave.startsWith("vd")){
            VeDem vd = new VeDem();
            if(noiVeDem.KiemTraMV(mave))
            {
                KiemTraMaVe ktmv = new KiemTraMaVe();
                ktmv.setVisible(true);
                noiVeDem.TimVeDem(vd, mave);
                ktmv.txtMaVe.setText(vd.getMaVe());
                ktmv.txtMaXe.setText(vd.getMaXe());
                ktmv.txtLoaiXe.setText(vd.getLoaiXe());
                ktmv.txtTGVao.setText(vd.getNgayGui());
                DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                // tạo 1 đối tượng có định dạng thời gian dd-MM-yyyy
                Date dateRa = new Date(); // lấy thời gian hệ thống
                String stringDateRa = dateFormat.format(dateRa);
                ktmv.txtTGRa.setText(stringDateRa);
                //tính số ngày xe gửi
                int sotien;
                sotien = vd.tinhTienVD(vd, vd.getNgayGui(), stringDateRa);
                ktmv.txtSoTien.setText(String.valueOf(sotien));
                ktmv.setVisible(true); 
            }
            else{
            JOptionPane.showMessageDialog(null,"Mã vé không tồn tại");
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Mã vé không tồn tại");
        }
    }//GEN-LAST:event_kiemTraVeRaActionPerformed

